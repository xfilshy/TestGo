package com.xue.bean;

public class WalletInfo {

    private String id;

    private String uid;

    private float diamond;

    private float frozenDiamond;

    private float nCoin;

    private float frozenNCoin;

    private float encashmentTotal;

    private float incomeNCoinTotal;

    private float money;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public float getDiamond() {
        return diamond;
    }

    public void setDiamond(float diamond) {
        this.diamond = diamond;
    }

    public float getFrozenDiamond() {
        return frozenDiamond;
    }

    public void setFrozenDiamond(float frozenDiamond) {
        this.frozenDiamond = frozenDiamond;
    }

    public float getNCoin() {
        return nCoin;
    }

    public void setNCoin(float nCoin) {
        this.nCoin = nCoin;
    }

    public float getFrozenNCoin() {
        return frozenNCoin;
    }

    public void setFrozenNCoin(float frozenNCoin) {
        this.frozenNCoin = frozenNCoin;
    }

    public float getEncashmentTotal() {
        return encashmentTotal;
    }

    public void setEncashmentTotal(float encashmentTotal) {
        this.encashmentTotal = encashmentTotal;
    }

    public float getIncomeNCoinTotal() {
        return incomeNCoinTotal;
    }

    public void setIncomeNCoinTotal(float incomeNCoinTotal) {
        this.incomeNCoinTotal = incomeNCoinTotal;
    }

    public float getMoney() {
        return money;
    }

    public void setMoney(float money) {
        this.money = money;
    }

}
